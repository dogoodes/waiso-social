package com.waiso.social.framework.json;

public class JSONConstant {

	public Object valor;
	
	public JSONConstant(){
		
	}
	public JSONConstant(Object valor){
		this.valor = valor;
	}
	
	public Object getValor(){
		return this.valor;
	}
	
	public void setValor(Object valor){
		this.valor = valor;
	}
	
}
